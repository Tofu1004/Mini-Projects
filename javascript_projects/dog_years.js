//setting a variable to my age
let myAge = 30;

//setting a numerical variable that can be changed
let earlyYears = 2;
earlyYears *= 10.5;

//setting a variable that is my age - 2
var laterYears = myAge -= 2;

//multiplying my later years by 4 to get # of dog years
laterYears *= 4;

//writing a variable for my age in dog years
var myAgeInDogYears = earlyYears + laterYears

//setting my name to all lowercase letters
const myName = 'Tofu';
console.log(myName.toLowerCase());

//displaying my name, human age, and dog age
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)

